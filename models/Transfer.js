import mongoose from "mongoose";

const transferSchema = new mongoose.Schema({
    fromUserId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
        required: true
    },
    toUserId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    amount: {
        type: Number,
        required: true
    },
    date: {
        type: Date, 
        default: Date.now()
    }
});

const Transfer = mongoose.model('Transfer', transferSchema)

export default Transfer
